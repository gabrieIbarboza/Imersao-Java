public class Content {
    
    private final String title;
    private final String imageURL;

    // Constructors
    public Content(String title, String imageURL) {
        this.title = title;
        this.imageURL = imageURL;
    }

    // Getters
    public String getTitle() {
        return title;
    }
    public String getImageURL() {
        return imageURL;
    } 

}
